package bity.zamiany_systemów;

/**
 * Wykonaj na kartce następujące równania:
 * -7 & 2
 * -30 | 10
 * -11 & 8
 * -26 >> 2
 * -36 << 3
 * -39 & 13
 * -12 & 18
 * -40 >> 4
 * następnie, możesz napisać program, który wypisze wyniki powyższych równań w celu porównania ich ze swoimi wynikami.
 */
public class Zad_04 {
    public static void main(String[] args) {
        System.out.println("7 & 2 = " + (7 & 2));
        System.out.println("30 | 10 = " + (30 | 10));
        System.out.println("11 & 8 = " + (11 & 8));
        System.out.println("26 >> 2 = " + (26 >> 2));
        System.out.println("36 << 3 = " + (36 << 3));
        System.out.println("39 & 13 = " + (39 & 13));
        System.out.println("12 & 18 = " + (12 & 18));
        System.out.println("40 >> 4 = " + (40 >> 4));
    }
}
